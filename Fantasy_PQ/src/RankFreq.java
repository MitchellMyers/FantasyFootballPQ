public class RankFreq {

    private int rank;
    private int freq;

    public RankFreq(int rank, int freq) {
        this.rank = rank;
        this.freq = freq;
    }

    public int getRank() {
        return this.rank;
    }

    public int getFreq() {
        return this.freq;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }

    public void setFreq(int freq) {
        this.freq = freq;
    }

}
